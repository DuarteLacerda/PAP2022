            Aqueduto da Quinta do Perdigão<br><br>
            Uma descoberta arqueológica no concelho da Lourinhã.
        </h1>
        <h2 class="desc-aqueduto">
            "Desde sempre a água foi indispensável para a fixação de uma comunidade; contudo, nem sempre as condições
            naturais se assumiram como condições necessárias e suficientes para tal. O desenvolvimento técnico permitiu que
            a ocupação humana de origem limitada às imediações de rios, lagos e nascentes de água doce se estendesse a áreas
            consideradas à partida inóspitas.
            <br><br>
            Ao longo da nossa história, foram construídos sistemas de captação, armazenamento, condução, distribuição e
            evacuação de águas. Vestígios arqueológicos destes sistemas construídos, alterados, abandonados e degradados
            testemunham várias soluções adoptadas, onde se procurou complementar défices locais e regionais através da
            condução de água a distâncias consideráveis.
            <br><br>
            A construção de vastos sistemas que suportaram abastecimentos estáveis e permanentes não se limitou ao
            preenchimento de necessidades básicas de uma população ou à melhoria das condições de determinadas actividades
            económicas. Estruturas notáveis podem indicar que a água se assumia como produto de luxo.
            <br><br>
            É com base nos conhecimentos desenvolvidos pela Arquitectura Romana na construção de aquaeductus (aquedutos) que
            fazemos uma análise, embora linear, da influência e perduração de metodologias de condução de águas livres
            verificada num exemplar do nosso concelho construído já num período contemporâneo.
            <br><br>
            Construído no século XVIII, com um desenvolvimento total aproximado de mil e quatrocentos metros de condução de
            águas livres, foi edificado um aqueduto subterrâneo com cerca de setecentos metros de desenvolvimento com
            paredes rectas e tectos abobadados.
            <br><br>
            Este aqueduto é dotado de acessos pedonais, câmaras de inspecção, tanques de sedimentação e tanques de
            armazenamento. Esta estrutura terá sido criada com o propósito de um abastecimento estável e permanente à Quinta
            do Perdigão, tendo a capacidade de armazenar constantemente cerca de novecentos metros cúbicos de água,
            abastecer um tanque de distribuição de vinte metros cúbicos, entre outros fins, usando técnicas de desnível,
            pressão e condução de águas por queda.
            <br><br>
            Escavado em arenitos e argilas magras, com um desnível variável entre um e sete metros em relação à cota de
            superfície, com cerca de um metro e setenta centímetros de altura e cerca de sessenta centímetros de largura.
            <br><br>
            Foi necessário extrair mais de mil metros cúbicos de pedra e sedimentos para criar esta obra majestosa que,
            posteriormente, foi revestida com alvenaria insossa ou seca (construção com pedras justapostas sem argamassa).
            De um traço geral, as únicas secções e/ou áreas específicas do aqueduto só não estão em seu pleno funcionamento
            por falta de manutenção ou por uma parca preservação.
            <br><br><br>
            Ainda é possível visitar algumas das secções que resistiram à evolução da sociedade e desenvolvimento urbano.
            Ainda é usado para diversos fins, embora de uma forma rudimentar e de parca manutenção. Os actuais proprietários
            de alguns dos terrenos onde este aqueduto se observa procuram cuidar e preservar esta magnífica obra,
            registando-se apenas o aterro de uma das câmaras de inspecção, aquando da construção do Campo de Futebol das
            Papagovas a um extremo de uma das secções, e a destruição de, pelo menos, duas câmaras de inspecção, no início
            de outra secção do aqueduto.
            <br><br>
            Com a publicação Contributos para uma Carta Arqueológica (Volume 1) – O Aqueduto da Quinta do Perdigão, que
            entretanto será divulgada pelo Centro de Estudos Históricos da Lourinhã, far-se-á um levantamento mais
            aprofundado de todo o complexo de condução de águas livres bem como a representação topográfica não só do
            aqueduto como de todos os elementos que compõem este edificado.
            <br><br>
            Este documento surge com o intuito de conservar na memória do concelho a existência deste aqueduto bem como
            servir como ponto de partida para um estudo deste património concebido há séculos que, claramente, mostra a
            preservação de conhecimentos desenvolvidos milénios antes. Este património edificado é apenas um dos inúmeros
            exemplos de património do concelho que merecem atenção e zelo a fim de uma contínua preservação."
            <br><br>
            in: Jornal O Alvorada, 1 de abril 2016
