        <h1 class="titulo-saoLourenco">A Presença Romana em São Lourenço dos Francos</h1>
        <h2 class="desc-saoLourenco">
            São Lourenço dos Francos localiza-se na União das freguesias de Miragaia e Marteleira Concelho da Lourinhã, na
            margem direita do Rio Grande, a uma altitude de cerca de 25 metros.
            <br><br><br>
            Há cerca de onze séculos que a documentação alude a construções na zona da Igreja. Frei António da Purificação,
            em meados do séc. XVII, refere na sua “Crónica da Antiquíssima Província de Portugal da Ordem dos Eremitas de
            Santo Agostinho” que o Mosteiro de São Lourenço dos Francos terá sido fundado no ano de 890, “na villa de
            Monardo” dos Francos. Segundo Frei João de Santo Estevão na “Crónica da Fundação do Mosteiro de S. Lourenço”
            refere um “… solho de casa debaixo do chão, ladrilhado de largos e grossos ladrilhos, (…) indício de que houve
            ali casas”.
            <br><br><br>
            Na publicação de 1987 de Mário Baptista Pereira, “Lourinhã Memórias da sua região”, dá-nos conta do seguinte: “…
            quando o antigo proprietário da Quinta do Perdigão Francisco Romeiro da Fonseca, mandou arrotear muito fundo a
            propriedade que adquirira junto de sua Quinta e em frente da Egreja referida, os quais objectos constavam,
            alicerces, telhas, etc.”
            <br><br><br>
            Cinco anos depois, Fr. Henrique Perdigão, em “Subsídios para a História da Ribeira dos Palheiro”, divulga
            informações pormenorizadas de achados arqueológicos dos quais destacamos os seguintes: “… quase no sopé do
            monte, perto do rio, nasce um olho de água permanente. Pesquisada esta nascente verificou-se que a corrente
            vinha por um cano feito com artificio Romano. (…) nas proximidades da Joaria, apareceram de mistura com a terra,
            algumas tijoleiras, pedaços de utensílios domésticos, duas ânforas de barro muito antigo, uns pedaços de ferros
            velhos e duas argolas (…), além de uma pedra curiosa em forma de machado.” Durante a abertura de um poço na
            “Quinta da Junceira, à profundidade de três metros, foram encontrados, com certa abundância, pedaços de
            tijoleira e cerâmica antiga.”
            <br><br><br>
            Neste conjunto de excertos podemos verificar a abundância de referências de potencial arqueológico, de diversas
            épocas, todas na área da Igreja e sua envolvente. Mas os indícios de potencial arqueológico sobem de nível
            quando olhamos para outros factos. No livro “Portugal Romano” editado em 1974, da autoria do arqueólogo Jorge
            Alarcão, é-nos dado a conhecer uma inscrição funerária mandada fazer por Júlia Máxuma, consagrada a Caio Júlio
            Lauro de 41 anos. Este mesmo aparece numa dedicatória a Marco Aurélio (imperador romano do séc. II) encontrada
            em São Tomás das Lamas, concelho do Cadaval. “Aparentemente, Caio Júlio Lauro foi um do quattuorviri de
            Eburobrittium no tempo de Marco Aurélio e teria a sua villa, na qual foi sepultado, perto de S. Lourenço dos
            Francos.” Ou seja, era um dos quatro magistrados da cidade romana em Óbidos.
            <br><br><br>
            No ano de 1976, José Beleza Moreira, publica na revista “Conimbriga” a anterior inscrição em conjunto com uma
            segunda consagrada a Júlia Máxima de 30 anos, mandada fazer pelo pai, Caio Júlio Severo, e sua mãe, Paterna.
            Existem outros documentos epigráficos com referências à família dos Júlios, no território de Eburobrittium e,
            estariam entre as grandes famílias de Eburobrittium, defende o arqueólogo Vasco Gil Mantas, na sua tese de
            doutoramento “A rede viária romana da faixa Atlântica entre Lisboa e Braga”. As pessoas referidas nas inscrições
            da Igreja de São Lourenço do Francos “estarão certamente ligadas por laços de parentesco” defende José Beleza
            Moreira.
