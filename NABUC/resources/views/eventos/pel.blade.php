                <h1 class="titulo-pel">Participação no PEL- Plano Estratégico da Lourinhã</h1>
                <h2 class="desc-pel">
                    Inserida no topo da unidade Calcários do Vimeiro (pertencente às Camadas de Alcobaça) a qual aflora,
                    desde
                    Casais dos Netos, a Norte, até próximo de Casais do Arneiro, a Sul. Segundo o corte de F. P. Teixeira
                    Silva,
                    1989, a unidade é constituída, da parte média-baixa para o topo, por calcários por vezes fortemente
                    bioclásticos (algas calcárias), em geral com oncólitos e com grande abundância de macrorrestos,
                    evoluindo o
                    conjunto para calcários mais compactos e mais pobres em fauna. A sedimentação desta unidade, com uma
                    espessura de cerca de 160 m, deu-se num ambiente francamente marinho, de pouca profundidade e de fraca
                    energia, ainda que bem iluminado e oxigenado. Da base para o topo, foram notadas oscilações nas
                    características do ambiente marinho (fenómenos de regressão e transgressão da água do mar). Esta unidade
                    está sobre um provável anticlinal salífero (MANUPPELLA et al., 1999). A idade da formação Calcários do
                    Vimeiro é atribuída ao Kimeridgiano Superior devido ao estudo micrográfico do corte, realizado sob
                    orientação de M. M. Ramalho, que identificou a presença de AlveoseptajaccardiSchrodt,
                    Campbelliellastriata
                    (Carozzi) e Everticyclamminavirguliana Koechlin (MANUPPELLA et al., 1999).
                    <br>
                    O afloramento, constituído pelos Calcários do Vimeiro, rodeia um vale tifónico, onde se situa a Maceira,
                    originado pela extrusão das Margas de Dagorda, datadas do Hetangiano, injectada na falha da Lourinhã. Os
                    vales tifónicos, segundo a definição do eminente geólogo Paul Chauffat, são limitados por séries de
                    falhas e
                    têm o fundo levantado através dos terrenos mais recentes, e, em contacto com estes, em todo o seu
                    perímetro.
                    Estas áreas revestem-se geralmente de grande importância pelas nascentes hidrominerais que nelas brotam
                    e
                    pela presença de certos minerais, como gesso e sal-gema, entre outros factores (P. G. ANACLETO, 1965).
                    <br>
                    Neste caso particular, o gesso aflora no núcleo do diapiro da Maceira /Vimeiro (G. C. FRANÇA et al.,
                    1961)
                    sem, no entanto, ser objecto de qualquer exploração. Existe exploração, sim, de águas minerais pelas
                    Termas
                    do Vimeiro, situadas no vale do rio Alcabrichel, efectuando a captação de duas nascentes, junto ao
                    extremo
                    SE do afloramento Hetangiano designadas por Rainha Santa Isabel e Fonte dos Frades. As águas da primeira
                    emergem dos calcários enquanto as da segunda nascente chegam à superfície pela falha de contacto entre
                    as
                    margas e os calcários. Para além da falha da Lourinhã e do contacto diapírico observa-se, ainda, na
                    carta um
                    outro conjunto de falhas transversais à direcção da primeira. São os resultados de processos tectónicos
                    e
                    das fortes tensões a eles associadas, provocando também uma completa alteração na posição original dos
                    estratos. Assim, ao longo do afloramento, podem medir-se direcções e pendores muito diversos.
                    <br>
                    O cabeço onde está instalada a Cova do Texugo possui estratos que inclinam cerca de 15o para NW. A sua
                    vertente oeste é muito abrupta e o desnível de cotas da superfície do terreno, desde o seu ponto mais
                    alto
                    até ao leito dos cursos de água que ladeiam o cabeço é apreciável, pois, vai desde perto de 65 m até
                    cerca
                    de 35 m a este e 45 m a oeste, respectivamente. O fundo do vale associado à vertente oeste assemelha-se
                    a um
                    pequeno Canyon que se terá formado pela acção do rio.
                    <br>
                    O complexo das Grutas do Cabeço da Pedra do Sino tem uma extensão total de, aproximadamente, 900 m de
                    galerias de média dificuldade de progressão, seguindo as orientações da fracturação (diaclases) do
                    extractocalcário em que se insere. Estas orientações, quase perpendiculares, promovem um rendilhado de
                    galerias labirínticas proporcionando uma exploração complexa, apenas conseguida pelo suporte
                    topográfico.
                    <br>
                    O sistema possui, na totalidade, onze entradas que se subdividem em três zonas do cabeço em estudo. Na
                    zona
                    norte do Cabeço, encontra-se, a oeste, a Cova do Urso, gruta que atravessa o Cabeço de oeste para este,
                    com
                    galerias em forma oval/vertical com máxima altura de 8 m, medianamente concrecionada, aumentando para o
                    seu
                    interior e apresentando, com frequência, estalactites e estalagmites. Ao nível do solo, os enchimentos
                    de
                    argila são frequentes na parte final da gruta. Esta gruta tem apenas ligação com a Cova da Chifruda, por
                    intermédio de uma galeria muito estreita (0,20 m) e alta (4 m).
                    <br>
                    A Cova da Chifruda (zona norte) possui uma entrada semi-vertical e, no seu interior, um poço de 4 m que
                    garante o acesso para a mesma cota da Cova do Urso. No seu interior, verifica-se a existência de
                    galerias
                    mestres (segundo as diaclases) e salas muito baixas, possuindo ainda inúmeros depósitos de argila e
                    zonas
                    bastante concrecionadas. Esta gruta, por sua vez, não apresenta qualquer ligação suficientemente ampla
                    que
                    permita a passagem para o restante complexo (Cova do Texugo e Pedra do Sino), apesar da proximidade (38
                    m) e
                    da idêntica disposição de galerias. Na zona central, e viradas para oeste, estão presentes a Cova da
                    Presa e
                    a Lapa do Meio. Estas duas grutas, de pequena dimensão, representam antigas entradas para todo o
                    sistema,
                    encontrando-se, neste momento, obstruídas com argila consolidada de difícil desobstrução. Ainda assim,
                    observam-se as galerias mestres orientadas para o interior do cabeço. Estas pequenas entradas
                    apresentam-se
                    com um enchimento de argila muito elevado ao nível do solo. Também na zona central, mas a este, existe a
                    Lapa da Pulga, uma das quatro entradas, a este, para o sistema principal de galerias. A Lapa da Pulga
                    possui, em toda a sua extensão e ao nível do solo, grandes quantidades de argila. A ligação para a Cova
                    do
                    Texugo é conseguida por intermédio de uma galeria com um desnível de 4 m, mas muito estreita (0,30 m).
                    <br>
                    Na zona sul do cabeço, encontra-se a parte mais importante (conhecida) deste sistema, com uma entrada a
                    Oeste (Cova do Texugo), quatro a Este (Pedra do Sino, Lapa da Luz, Cova do Mongo e Lapa da Pinha) e uma
                    entrada vertical no topo do cabeço (Pedra do Sino II). Nesta zona, é iminente o elevado número de
                    galerias
                    mestres (altura de 6 m e de configuração diaclase/vertical), que se interceptam sucessivamente umas com
                    as
                    outras, por intermédio de galerias de inferior secção, mais estreitas e de menor altura, verificando-se
                    a
                    existência de abatimentos nessas mesmas intercepções. Nestas galerias, é raro observar-se
                    concrecionamentos
                    do calcário, apresentando-se uniforme e com marcas de erosão física. Visualizando agora a secção destas,
                    pode-se constatar que existem dois níveis distintos de formação; no topo, encontram-se com frequência
                    mais
                    largas, variando de 3 m a 1 m, com uma variação brusca para o resto da galeria, que praticamente
                    mantem-se
                    constante (aprx. 0,50 m), afunilando um pouco para a base. Este tipo se secção está directamente ligado
                    com
                    a velocidade de formação das galerias, muito lento no início, aumentando para a base. A existência de
                    zonas
                    com preenchimento de argilas também é visível no sistema, que advém da subida e descida do nível
                    freático,
                    ou seja, este variou a sua cota ao longo do tempo, permanecendo longos períodos no início, decrescendo
                    para
                    o final (Topografia em Anexo 1).
                    <br>
                    Durante os trabalhos de desobstrução realizados numa das lapas do complexo cársico de Maceira/Ribamar,
                    foram
                    postos a descoberto dois dentes caninos de javali que apresentam afeiçoamento e perfurações. Trata-se de
                    peças destinadas a adorno, possivelmente com funções de carácter mágico-simbólico. Na proximidade destes
                    artefactos surgiram os fragmentos de dois ossos humanos, o que sugere um contexto funerário. Apesar de
                    próximos e em área restrita, os achados surgiram desconexos e sem demais espólio que possibilite uma
                    leitura
                    cronológica concreta.
                    <br>
                    O desimpedimento da lapa designada por Cova da Presa, a partir do exterior, foi uma das tarefas
                    decorrentes
                    de um projecto global de estudo do complexo de galerias de origem cársica existente nos afloramentos
                    calcários jurássicos de Maceira (Lourinhã) /Ribamar (Torres Vedras), zona que se divide entre os
                    distritos
                    de Leiria e de Lisboa (REGALA, R. & LUÍS, R. 2002; REGALA, F. & GOMES, E. 2002). O conjunto norte é
                    constituído pela Cova do Urso e Cova da Chifruda, que estão interligadas. Não se conhece actualmente
                    qualquer conduta de ligação entre os conjuntos sul e norte, apesar de se encontrarem muito próximos
                    (cerca
                    de 38m). Esta situação confere particular relevância à zona intermédia, ainda que aí sejam apenas
                    conhecidas
                    pequenas cavidades, significativamente preenchidas pelos sedimentos. Foi por este motivo que se investiu
                    com
                    particular contumácia na desobstrução da Cova da Presa, a qual se encontra posicionada na interface dos
                    dois
                    principais subsistemas. Assim, os trabalhos foram promovidos na expectativa de serem alcançadas galerias
                    inexploradas que poderiam vir a estabelecer a ligação de todo o complexo. A Cova da Presa e a Cova do
                    Meio
                    são cavidades adjacentes que distam cerca de seis metros entre si.
                    Têm desenvolvimento paralelo este-oeste, desembocando para ocidente. As duas entradas estariam
                    anteriormente
                    ligadas conformando um abrigo que abateu em período indeterminado.
                    <br>
                    Reconhecendo efectivamente o impacto não só turístico como também económico, sendo a base do mesmo este
                    fantástico exemplar de cavidades do concelho, verificando-se não só o uso deste complexo cavernícola
                    para
                    fins recreativos por parte dos cidadãos no geral, como por empresas que fornecem condições para a
                    população
                    todas as condições de práticas de desportos radicais, nomeadamente a prática do espeleo-turismo, como
                    também
                    é alvo de “jogos” on-line (Geocaching), estando referenciada a “Gruta Cova do Texugo” vimos deste modo
                    alertar não só algumas problemáticas resultantes do contínuo impacto do seu uso fruto como também
                    indicar
                    algumas das soluções viáveis para a requalificação do complexo bem como da área envolvente que
                    claramente
                    merece o mesmo respeito e atenção.
                    <br>
                    Como resultado de práticas erradas no passado, que não são compatíveis com aquilo que hoje é considerado
                    como conduta de espeleólogo, como também da visita de populares sem formação cívica relacionada com a
                    boa
                    preservação cavernícola, regista-se a existência de escrituras e gravações de registo como prova de
                    presença
                    na cavidade, sendo usadas tintas e sprays de grafiti bem como a deposição de pequenos lixos (garrafas
                    plásticas, pilhas, sacos de plástico, etc…).
                    <br>
                    Como acima referido “Estas orientações, quase perpendiculares, promovem um rendilhado de galerias
                    labirínticas proporcionando uma exploração complexa, apenas conseguida pelo suporte topográfico”,
                    problemática esta que obrigou a várias entidades promocionais de eventos de recreio e passeios
                    espeleo-turísticos, usando tintas e similares como forma/solução para sinalizar sentidos de saídas e/ou
                    rotas sendo mais notórios estes indicadores de direcção em grande parte dos cruzamentos de diaclases em
                    diversos tons (verdes e vermelhos em predominância), o que claramente indica que foi feito por, pelo
                    menos,
                    duas entidades diferentes.
                    <br>
                    Os acessos principais são de igual forma identificados com tintas e escrituras mais uma vez não só
                    erradamente feitas como também a contínua aplicação de uma metodologia não reconhecida pela conduta e
                    boas
                    práticas da espeleologia.
                    <br>
                    Registando o acima referido, é de nossa obrigação moral, bem como boa prática dos estatutos pelo qual
                    nos
                    regemos, alertar para este facto como também tomar esta oportunidade de sugerir um conjunto de soluções
                    tendo como fim, e unicamente, o intuito de minimizar os impactos causados pelas visitas constantes a
                    este
                    complexo, criando em simultâneo condições para requalificar a zona, pois claramente a Maceira, como todo
                    o
                    concelho, beneficia com a prática de requalificação da zona.
                    <br>
                    Deste modo, lançamos o desfio de se realizar:
                    <br>
                    Limpeza dos lixos ainda existentes no interior da cavidade (a grande maioria já foi retirada por nós na
                    última avaliação do estado de conservação da cavidade);
                    Eliminação, mediante o possível, causando o menor impacto possível nas cavidades, das tintas nas paredes
                    das
                    galerias e acessos do complexo;
                    Colocação de indicadores das diversas saídas no interior da cavidade, usando aço inox 316 (sendo este o
                    aço
                    mais resistente aos elementos) e eliminando os inúmeros indicadores “grafitados” no interior da cavidade
                    (sendo alguns susceptíveis de erro);
                    Limpeza e criação de acessos mais cómodos a todas as entradas do complexo com a construção de um trilho
                    circundante ao relevo cársico com o menor impacto possível na fauna e flora local;
                    Criação de um corrimão de segurança numa passagem no interior;
                    Colocação de placas identificativas em todos os acessos ao interior do complexo cársico estando presente
                    no
                    mesmo:
                    Identificação da cavidade;
                    Topografia da cavidade com indicador “você está aqui”;
                    Números de emergência;
                    Condutas e boas práticas cavernícolas;
                    Descrição da cavidade preservando os direitos de autor (AESDA);
                    Descrição dos trabalhos arqueológicos nas cavidades em que se registou a existência de espólio bem como
                    a
                    indicação dos detentores do espólio;
                    <br><br>
                    Com este conjunto de pequenos trabalhos realizados, consideramos que será praticada uma requalificação
                    da
                    área e, por consequência, projectar a zona para um maior impacto turístico somado a um melhor grupo de
                    condições para a venda do produto “espeleo-turismo” já praticado por empresas da zona, nomeadamente a
                    empresa “Tudo Aventura”. Contudo temos a noção de que esta intervenção irá carecer de um acompanhamento
                    (no
                    mínimo anual), como tal o compromisso não se resume a um acto isolado.
