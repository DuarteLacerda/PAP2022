            <h1 class="titulo-cong">
                O papel do voluntariado na ciência e salvaguarda do Património cultural e natural do
                Planalto das Cesaredas: Contribuições do NABUC
            </h1>
            <h2 class="desc-cong">
                <br><br>
                O Planalto das Cesaredas é detentor de um património geológico (particularmente espeleológico e
                paleontológico), arqueológico, biológico e etnográfico de grande importância para o oeste. Esta estrutura
                aplanada, de características carbonatadas, que se divide entre os concelhos de Lourinhã, Óbidos, Peniche e
                Bombarral, é o centro da atividade do Núcleo de Amigos dos Buracos das Cesaredas (NABUC). O NABUC surge em
                2012 como intuito de cadastrar as grutas conhecidas do Planalto das Cesaredas e em 2014 torna-se secção de
                Espeleologia do Centro de Estudos Históricos da Lourinhã (CEHL). A sua equipa é constituída por voluntários
                espeleólogos, geólogos, paleontólogos, arqueólogos e socorristas.
                <br><br><br>
                O que inicialmente foi tomado como um cadastro simples de se organizar, considerando que registava-se um
                numero bastante reduzido de cavidades conhecidas, verifica-se agora que pode demorar perto de uma década
                para fazer o inventario de todas as georreferencias já corretamente identificadas, ao que podemos somar um
                tempo indeterminado para as cavidades que irão surgir com os trabalhos de enquadramento geográfico e
                reconhecimento de padrões após os levantamentos topográficos e a sua implantação de acordo com a geografia
                da area em que se enquadram.
                <br><br><br>
                O NABUC trabalha agora na localização, cadastro, topografia, estudo geológico e arqueológico das cavidades,
                sobretudo as do concelho da Lourinhã, tendo já cadastrado mais de 80 cavidades dos quatro concelhos. Tem
                desenvolvido parcerias com diversas parcerias com diversas instituições, como a Direção Geral de Património
                e Cultura, e a Patrimonium, contribuindo para a deteção e Salvaguarda de achados arqueológicos nestas
                cavidades e fora delas, como necrópoles neolíticas, fornos de cal antigos, aquedutos (aqueduto da Quinta do
                Perdigão) e para a relocalização de grutas com interesse histórico para a Arqueologia e Geologia nacionais,
                nomeadamente a relocalização das cavidades Lapa Furada e Cova da Moura (N.Delgado, 1867). Foram sinalizadas
                situações de perigo eminente para o património Arqueológico, como a vandalização da porta e consequente
                acesso não controlado da Gruta da Feteira.
                <br><br><br>
                O NABUC além de contribuir para o levantamento científico do Planalto das Cesaredas, colabora com as
                autoridades locais na busca e salvamento de pessoas desaparecidas em contexto cavernícola, estes voluntários
                organizam também atividades de divulgação do património do Planalto e de sensibilização da sua proteção
                através de ações de formação e participação em feiras e congressos. Enfatizam na importância do Planalto das
                Cesaredas enquanto estrutura geológica, estrutura de recarga aquífera, casa de enumeras espécies, enquanto
                local com tradições próprias e depósito de vestígios importantes da história do Homem.
                <br><br><br>
                O NABUC não é pioneiro na atividade desenvolvida, regista-se inúmeros casos de trabalhos de excelência
                desenvolvidos por outras associações nomeadamente a AESDA sendo exemplo claro da Gruta Casal da Lebre e o
                Espeleo Clube Torres Vedras nos trabalhos desenvolvidos na Gruta Casa da Moura.
